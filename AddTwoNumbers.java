public class AddTwoNumbers {

    public static void main(String[] args) {

        int num1 = 5, num2 = 15, sum;
        sum = num1 + num2;

        System.out.println("Sum of these numbers: "+sum);


        // increase it to three numbers
        int num3=10;
        sum=sum+num3;
        System.out.println("Sum of three numbers: "+sum);
    }
}